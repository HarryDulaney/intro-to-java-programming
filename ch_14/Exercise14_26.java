package ch_14;

import javafx.application.Application;
import javafx.scene.Scene;
import javafx.scene.layout.GridPane;
import javafx.scene.layout.Pane;
import javafx.scene.paint.Color;
import javafx.scene.shape.Circle;
import javafx.scene.shape.Line;
import javafx.scene.text.Text;
import javafx.stage.Stage;

import java.util.Calendar;
import java.util.GregorianCalendar;

/**
 * 14.26 (Use the ClockPane class) Write a program that displays two clocks. The hour,
 * minute, and second values are 4, 20, 45 for the first clock and 22, 46, 15 for the
 * second clock, as shown in Figure 14.51c.
 */
public class Exercise14_26 extends Application {
    double paneWidth = 475;
    double paneHeight = 275;

    @Override
    public void start(Stage primaryStage) {

        ClockPane clock1 = new ClockPane(4, 20, 45);
        ClockPane clock2 = new ClockPane(22, 46, 15);

        GridPane pane = new GridPane();
        pane.add(clock1, 0, 0);
        pane.add(clock2, 1, 0);

        clock1.setW(250);
        clock1.setH(250);
        clock2.setW(250);
        clock2.setH(250);

        Scene scene = new Scene(pane, paneWidth, paneHeight);
        primaryStage.setTitle(getClass().getName());
        primaryStage.setScene(scene);
        primaryStage.show();
    }


    /**
     * ClockPane is a Pane that displays a clock.
     */
    class ClockPane extends Pane {
        private int hour;
        private int minute;
        private int second;

        private double w = 250, h = 250;

        /**
         * Construct a default clock with the current time
         */
        public ClockPane() {
            setCurrentTime();
        }

        public ClockPane(int hour, int minute, int second) {
            this.hour = hour;
            this.minute = minute;
            this.second = second;
            paintClock();
        }

        public int getHour() {
            return hour;
        }

        public void setHour(int hour) {
            this.hour = hour;
            paintClock();
        }

        public int getMinute() {
            return minute;
        }

        public void setMinute(int minute) {
            this.minute = minute;
            paintClock();
        }

        public int getSecond() {
            return second;
        }

        public void setSecond(int second) {
            this.second = second;
            paintClock();
        }

        public double getW() {
            return w;
        }

        public void setW(double w) {
            this.w = w;
            paintClock();
        }

        public double getH() {
            return h;
        }

        public void setH(double h) {
            this.h = h;
            paintClock();
        }

        public void setCurrentTime() {
            Calendar calendar = new GregorianCalendar();
            this.hour = calendar.get(Calendar.HOUR_OF_DAY);
            this.minute = calendar.get(Calendar.MINUTE);
            this.second = calendar.get(Calendar.SECOND);
            paintClock(); // Repaint the clock
        }

        protected void paintClock() {
            double clockRadius = Math.min(w, h) * 0.8 * 0.5;
            double centerX = w / 2;
            double centerY = h / 2;

            Circle circle = new Circle(centerX, centerY, clockRadius);
            circle.setFill(Color.WHITE);
            circle.setStroke(Color.BLACK);
            Text t1 = new Text(centerX - 5, centerY - clockRadius + 12, "12");
            Text t2 = new Text(centerX - clockRadius + 3, centerY + 5, "9");
            Text t3 = new Text(centerX + clockRadius - 10, centerY + 3, "3");
            Text t4 = new Text(centerX - 3, centerY + clockRadius - 3, "6");
            double sLength = clockRadius * 0.8;
            double secondX = centerX + sLength *
                    Math.sin(second * (2 * Math.PI / 60));
            double secondY = centerY - sLength *
                    Math.cos(second * (2 * Math.PI / 60));
            Line sLine = new Line(centerX, centerY, secondX, secondY);
            sLine.setStroke(Color.RED);
            double mLength = clockRadius * 0.65;
            double xMinute = centerX + mLength *
                    Math.sin(minute * (2 * Math.PI / 60));
            double minuteY = centerY - mLength *
                    Math.cos(minute * (2 * Math.PI / 60));
            Line mLine = new Line(centerX, centerY, xMinute, minuteY);
            mLine.setStroke(Color.BLUE);

            double hLength = clockRadius * 0.5;
            double hourX = centerX + hLength *
                    Math.sin((hour % 12 + minute / 60.0) * (2 * Math.PI / 12));
            double hourY = centerY - hLength *
                    Math.cos((hour % 12 + minute / 60.0) * (2 * Math.PI / 12));
            Line hLine = new Line(centerX, centerY, hourX, hourY);
            hLine.setStroke(Color.GREEN);
            getChildren().clear();
            getChildren().addAll(circle, t1, t2, t3, t4, sLine, mLine, hLine);
        }
    }
}
